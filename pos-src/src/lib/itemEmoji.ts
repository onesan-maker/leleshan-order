const DEFAULT_EMOJI = "🍲";

export function getItemEmoji(name: string | undefined | null): string {
  if (!name) return DEFAULT_EMOJI;
  for (const [pattern, emoji] of EMOJI_RULES) {
    if (pattern.test(name)) return emoji;
  }
  return DEFAULT_EMOJI;
}
